public class IntegerValue {
    public static void main(String[] args) {
        System.out.println("10  1357         =" + 1357);
        int i = 0b10011001; // int

        System.out.println("0b1001001 =" + i);

        long l = 0XADEF; // lone
        System.out.println("16  0XADEF   =" + i);

        short s = 01357; // short 
        System.out.println("8 01357    = " + s);
    }
}